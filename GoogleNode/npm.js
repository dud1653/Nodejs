/*
    1. npm 버전확인
    % npm --version

    2. npm을 사용하여 모듈설치
    npm install <모듈 이름>

    % npm install express
    express는 Node.js 웹 프레임워크

    3. 글로벌 vs 로컬 모듈 설치
    기본적으로 npm은 모듈을 로컬모드로 설치
    로컬 모드 : 패키지를 명령어를 실행한 디렉토리안에 있는 node_modules에 설치하는 것
    글로벌 모드 : 시스템 디렉토리에 설치

    % sudo npm install express -g
*/